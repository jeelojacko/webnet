const buildAreaOfUseBoundsForUtm = (zoneNumber: number): CrsAreaOfUseBounds => {
  const lon0 = zoneNumber * 6 - 183;
  return {
    minLatDeg: 40,
    maxLatDeg: 84,
    minLonDeg: lon0 - 3.5,
    maxLonDeg: lon0 + 3.5,
  };
};

